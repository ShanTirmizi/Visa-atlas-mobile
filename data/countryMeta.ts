// Keep old name for backwards compat
export function getISTOffset(code: string): string | undefined {
  return getLondonOffset(code);
}

export function getLondonOffset(code: string): string | undefined {
  const meta = countryMeta[code];
  if (!meta) return undefined;

  const tz = meta.timezone;
  const match = tz.match(/UTC([+-])(\d+)(?::(\d+))?/);
  if (!match) return tz === "UTC+0" ? "Same as London" : undefined;

  const sign = match[1] === "+" ? 1 : -1;
  const hours = parseInt(match[2], 10);
  const minutes = parseInt(match[3] || "0", 10);
  const totalMinutes = sign * (hours * 60 + minutes);

  // London is UTC+0 (GMT) — in summer it's BST (UTC+1) but we use GMT as baseline
  const diff = totalMinutes;

  if (diff === 0) return "Same as London";
  const absHours = Math.floor(Math.abs(diff) / 60);
  const absMins = Math.abs(diff) % 60;
  const ahead = diff > 0 ? "ahead of" : "behind";
  if (absMins === 0) return `${absHours}h ${ahead} London`;
  return `${absHours}h ${absMins}m ${ahead} London`;
}
