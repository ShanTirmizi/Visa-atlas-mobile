/**
 * Returns a Google Flights search URL from London (LHR) to the given destination.
 */
export function getFlightSearchUrl(iataCode: string): string {
  return `https://www.google.com/travel/flights?q=flights+from+LHR+to+${encodeURIComponent(iataCode)}`;
}

/**
 * Returns a human-readable cost label for the given cost level.
 */
export function getCostLabel(level: 1 | 2 | 3): string {
  const labels: Record<1 | 2 | 3, string> = {
    1: "Budget",
    2: "Mid-range",
    3: "Luxury",
  };
  return labels[level];
}

/**
 * Returns a GBP symbol string for the given cost level.
 */
export function getCostSymbol(level: 1 | 2 | 3): string {
  const symbols: Record<1 | 2 | 3, string> = {
    1: "\u00a3",
    2: "\u00a3\u00a3",
    3: "\u00a3\u00a3\u00a3",
  };
  return symbols[level];
}
