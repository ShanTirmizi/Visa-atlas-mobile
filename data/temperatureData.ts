/**
 * Get the average high temperature for a city in a given month.
 * @param city - City name (must match key in cityTemperatures)
 * @param month - Month number, 1 = January, 12 = December
 * @returns Average high temp in Celsius, or null if not found
 */
export function getTemperature(city: string, month: number): number | null {
  const temps = cityTemperatures[city];
  if (!temps || month < 1 || month > 12) return null;
  return temps[month - 1];
}

/**
 * Compare a city's temperature with London for a given month.
 * @param city - City name
 * @param month - Month number (1-12)
 * @returns Comparison object or null if data is unavailable
 */
export function compareWithLondon(
  city: string,
  month: number
): {
  cityTemp: number;
  londonTemp: number;
  diff: number;
  warmerOrColder: string;
} | null {
  const cityTemp = getTemperature(city, month);
  const londonTemp = getTemperature("London", month);

  if (cityTemp === null || londonTemp === null) return null;

  const diff = cityTemp - londonTemp;

  let warmerOrColder: string;
  if (diff > 0) {
    warmerOrColder = "warmer";
  } else if (diff < 0) {
    warmerOrColder = "colder";
  } else {
    warmerOrColder = "same";
  }

  return { cityTemp, londonTemp, diff, warmerOrColder };
}

/**
 * Get a weather description based on temperature.
 */
export function getWeatherLabel(tempC: number): string {
  if (tempC <= 0) return "Freezing";
  if (tempC <= 10) return "Cold";
  if (tempC <= 18) return "Cool";
  if (tempC <= 24) return "Mild";
  if (tempC <= 30) return "Warm";
  if (tempC <= 35) return "Hot";
  return "Very hot";
}

/**
 * Find the best months to visit a city based on a preferred temperature range.
 * Defaults to a comfortable 18-28 C range.
 */
export function getBestMonths(
  city: string,
  minTemp: number = 18,
  maxTemp: number = 28
): number[] {
  const temps = cityTemperatures[city];
  if (!temps) return [];

  const months: number[] = [];
  for (let i = 0; i < 12; i++) {
    if (temps[i] >= minTemp && temps[i] <= maxTemp) {
      months.push(i + 1); // 1-indexed
    }
  }
  return months;
}

